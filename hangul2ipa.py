    with open(input_file_path, 'r', encoding='utf-8') as file_a:
        input_string = file_a.read()
        translated_string = translate_string(input_string, translation_table)
        with open(output_file_path, 'w', encoding='utf-8') as file_b:
            file_b.write(translated_string)

